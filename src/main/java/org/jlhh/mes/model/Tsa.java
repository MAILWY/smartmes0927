public class Tsa {
    private String tsaId;
    private String tsaLine;
    private String tsaRow;
    private String tsaTime;
    private String tsaUseflag;
    private String tsaDesc;

    public String getTsaId() {
        return tsaId;
    }

    public void setTsaId(String tsaId) {
        this.tsaId = tsaId;
    }

    public String getTsaLine() {
        return tsaLine;
    }

    public void setTsaLine(String tsaLine) {
        this.tsaLine = tsaLine;
    }

    public String getTsaRow() {
        return tsaRow;
    }

    public void setTsaRow(String tsaRow) {
        this.tsaRow = tsaRow;
    }

    public String getTsaTime() {
        return tsaTime;
    }

    public void setTsaTime(String tsaTime) {
        this.tsaTime = tsaTime;
    }

    public String getTsaUseflag() {
        return tsaUseflag;
    }

    public void setTsaUseflag(String tsaUseflag) {
        this.tsaUseflag = tsaUseflag;
    }

    public String getTsaDesc() {
        return tsaDesc;
    }

    public void setTsaDesc(String tsaDesc) {
        this.tsaDesc = tsaDesc;
    }
}
